using CommunityToolkit.Mvvm.ComponentModel;

using FluentWeather.Contracts.Services;
using FluentWeather.Models;
using FluentWeather.Models.Test;
using FluentWeather.Views;

using Microsoft.UI.Xaml.Navigation;
using System.Collections.ObjectModel;

namespace FluentWeather.ViewModels;

public partial class ShellViewModel : ObservableRecipient
{
    [ObservableProperty]
    private bool isBackEnabled;

    [ObservableProperty]
    private object? selected;

    public ObservableCollection<WeatherLocation> Locations { get; set; } = new();

    public INavigationService NavigationService
    {
        get;
    }

    public INavigationViewService NavigationViewService
    {
        get;
    }

    public ShellViewModel(INavigationService navigationService, INavigationViewService navigationViewService)
    {
        NavigationService = navigationService;
        NavigationService.Navigated += OnNavigated;
        NavigationViewService = navigationViewService;

        Locations.Add(Foo.FooWeather);  // Todo: Remove this line
    }

    private void OnNavigated(object sender, NavigationEventArgs e)
    {
        IsBackEnabled = NavigationService.CanGoBack;

        if (e.SourcePageType == typeof(SettingsPage))
        {
            Selected = NavigationViewService.SettingsItem;
            return;
        }

        var selectedItem = NavigationViewService.GetSelectedItem(e.SourcePageType);
        if (selectedItem != null)
        {
            Selected = selectedItem;
        }
    }
}
